// Generate the base64-embedded wasm module consumed by src/rust-layout/index.ts.
// The layout wasm is inlined so `@r4pm/components` works in any bundler/runtime with no
// asset-resolution config. Runs after wasm-pack (chained in the `build:wasm` npm script); also runs
// standalone in `prebuild` so a plain `pnpm build` regenerates it from an already-built .wasm.
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const pkgDir = join(here, "..", "src", "rust-layout", "pkg");
const wasmPath = join(pkgDir, "viz_layout_wasm_bg.wasm");
const gluePath = join(pkgDir, "viz_layout_wasm.js");
const outJs = join(pkgDir, "wasm-inline.js");
const outDts = join(pkgDir, "wasm-inline.d.ts");

// wasm-pack's `--target web` glue defaults to `new URL('..._bg.wasm', import.meta.url)` when init is
// called with no argument. We always pass an explicit source (inline bytes or a `setRustLayoutWasm`
// override), so that branch is dead, but bundlers (Vite) statically detect the `new URL(...)` and
// emit the whole .wasm as a *second*, unused asset alongside the inlined copy. Neuter it so only the
// embedded wasm ships. The public init API (accepting url/bytes/Module explicitly) is unchanged.
function neuterGlueDefaultUrl() {
  const src = readFileSync(gluePath, "utf8");
  const marker = "new URL('viz_layout_wasm_bg.wasm', import.meta.url)";
  if (!src.includes(marker)) {
    if (src.includes("__viz_layout_no_default_wasm_url")) return; // already patched
    throw new Error(
      `[gen-wasm-inline] expected \`${marker}\` in ${gluePath}; wasm-pack output shape changed - update the patch.`,
    );
  }
  const patched = src.replace(
    marker,
    "(() => { throw new Error('viz-layout wasm: no source (call setRustLayoutWasm) /*__viz_layout_no_default_wasm_url*/'); })()",
  );
  writeFileSync(gluePath, patched);
}

export function genWasmInline() {
  if (!existsSync(wasmPath)) {
    throw new Error(
      `viz-layout wasm not found at ${wasmPath}. Run \`pnpm build:wasm\` (needs the Rust + wasm-pack toolchain) to build it first.`,
    );
  }
  neuterGlueDefaultUrl();
  const b64 = readFileSync(wasmPath).toString("base64");
  writeFileSync(
    outJs,
    `// AUTO-GENERATED by scripts/gen-wasm-inline.mjs - do not edit. Base64 of viz_layout_wasm_bg.wasm.\nexport const wasmBase64 = "${b64}";\n`,
  );
  writeFileSync(outDts, `export declare const wasmBase64: string;\n`);
  const kb = (b64.length / 1024).toFixed(0);
  console.log(`[gen-wasm-inline] wrote wasm-inline.js (${kb} KB base64)`);
}

if (import.meta.url === `file://${process.argv[1]}`) {
  genWasmInline();
}
